/// Клиентская цензура нецензурных слов для академического раздела.
///
/// Применяется в двух местах:
///   • При **отправке** сообщения — текст автоматически цензурируется перед
///     передачей на сервер (отправляется уже «чистый» вариант).
///   • При **отображении** входящих сообщений — на случай если сообщение
///     было отправлено через другой клиент без фильтра.
///
/// Замена: первая буква слова сохраняется, остальные заменяются «*».
/// Пример: «хуй» → «х**», «пизда» → «п***а».
library profanity_filter;

class ProfanityFilter {
  ProfanityFilter._();

  // ── Список слов ───────────────────────────────────────────────────────────
  // Содержит базовые формы и наиболее частые производные.
  // Регистронезависим (флаг i в RegExp).
  static const List<String> _words = [
    // хуй и производные
    'хуй', 'хуя', 'хую', 'хуем', 'хуёв', 'хуев',
    'хуйня', 'хуйню', 'хуйни', 'хуйнёй',
    'нахуй', 'нахуя', 'похуй', 'похуя', 'похуйста',
    'захуй', 'захуярить', 'захуярил',
    'охуеть', 'охуел', 'охуела', 'охуели', 'охуенный', 'охуенно',
    'хуесос', 'хуесоса', 'хуйло',
    'похуист', 'похуизм',

    // пизда и производные
    'пизда', 'пизды', 'пизде', 'пизду', 'пиздой',
    'пиздец', 'пиздецу', 'пиздецом',
    'пиздёж', 'пиздежа', 'пиздёжник',
    'пиздить', 'пиздит', 'пиздил', 'пиздила', 'пиздили',
    'пиздануть', 'пизданул', 'пизданула',
    'распиздяй', 'распиздяйство',
    'пиздострадалец',

    // ёб / еб корень
    'ёбать', 'ебать', 'ебёт', 'ебёт', 'ебут', 'ебал', 'ебала', 'ебали',
    'ёбаный', 'ёбаная', 'ёбаное', 'ёбаные',
    'ебаный', 'ебаная', 'ебаное', 'ебаные',
    'заебать', 'заебал', 'заебала', 'заебали', 'заебался', 'заебалась',
    'заебали', 'заебало',
    'наебать', 'наебал', 'наебала', 'наебали', 'наебалово',
    'отъебись', 'отьебись', 'отъебать',
    'проёбать', 'проебать', 'проёбал', 'проебал',
    'ебло', 'еблан', 'еблана',
    'выёбываться', 'выёбывается', 'выёбывался',
    'уёбок', 'уёбище',
    'съёбаться', 'съёбался',
    'подъёбывать', 'подъёб',
    'ёбнуть', 'ёбнул', 'ёбнула', 'ёбнуться',
    'переёбнуться',

    // блядь и производные
    'блядь', 'бляди', 'бляди', 'блядей', 'блядям',
    'блядина', 'блядство', 'блядовать',
    'блять',  // распространённая форма

    // пиздить (в значении красть / бить) — дополнительные формы
    'пиздануть',

    // мудак и производные
    'мудак', 'мудака', 'мудаку', 'мудаком', 'мудаки', 'мудаков',
    'мудила', 'мудило',

    // залупа
    'залупа', 'залупы', 'залупе', 'залупу', 'залупой',

    // пиздатый
    'пиздатый', 'пиздатая', 'пиздато',

    // конкретные бранные
    'нихуя', 'нихуёвый', 'нихера',
    'хер', 'херня', 'херово', 'хернёй', 'херни',
    'пиздануть',

  ];

  // ── Скомпилированное RegExp — строится один раз ────────────────────────────
  static final RegExp _pattern = _buildPattern();

  static RegExp _buildPattern() {
    // Сортируем от длинных к коротким, чтобы длинные производные
    // совпадали раньше коротких базовых форм.
    final sorted = List<String>.from(_words)
      ..sort((a, b) => b.length.compareTo(a.length));

    final escaped = sorted.map(RegExp.escape).join('|');
    return RegExp(escaped, caseSensitive: false, unicode: true);
  }

  // ── Публичный API ─────────────────────────────────────────────────────────

  /// Заменяет нецензурные слова в [text]: сохраняет первую букву,
  /// заменяет остальные символами «*».
  static String censor(String text) {
    if (text.isEmpty) return text;
    return text.replaceAllMapped(_pattern, (m) {
      final word = m.group(0)!;
      if (word.length <= 1) return word;
      return word[0] + '*' * (word.length - 1);
    });
  }

  /// Возвращает true, если [text] содержит хотя бы одно запрещённое слово.
  static bool hasProfanity(String text) {
    if (text.isEmpty) return false;
    return _pattern.hasMatch(text);
  }
}
